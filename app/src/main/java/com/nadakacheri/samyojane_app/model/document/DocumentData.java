package com.nadakacheri.samyojane_app.model.document;

public class DocumentData {

    public Integer slNo;
    public String app_ID;
    public Integer docsID;
    public String docs_Name;

    public Integer getSlNo() {
        return slNo;
    }

    public void setSlNo(Integer slNo) {
        this.slNo = slNo;
    }

    public String getApp_ID() {
        return app_ID;
    }

    public void setApp_ID(String app_ID) {
        this.app_ID = app_ID;
    }

    public Integer getDocsID() {
        return docsID;
    }

    public void setDocsID(Integer docsID) {
        this.docsID = docsID;
    }

    public String getDocs_Name() {
        return docs_Name;
    }

    public void setDocs_Name(String docs_Name) {
        this.docs_Name = docs_Name;
    }
}
